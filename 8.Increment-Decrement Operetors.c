#include<stdio.h>
int main() {

	
/* Increment Operator ++     
	      Prefix Increment Operator    ++a
	      Postfix Increment Operator   a++
	Decrement Operetor --
	      Prefix Decrement Operator    --a
	      Postfix Decrement Operator   a--  */
	      
	int a = 2;
	
	printf("%d\n",a);      
	printf("%d\n",++a);
	printf("%d\n",a);
	printf("%d\n",a++);
	printf("%d\n\n",a);
	
	printf("%d\n",a);      
	printf("%d\n",--a);
	printf("%d\n",a);
	printf("%d\n",a--);
	printf("%d\n\n\n\n\n\n",a);
	
	
	
	int b = 2;		  
		  
	printf("%d\n",b);       
	printf("%d\n",b++);
	printf("%d\n\n",b);		  
		  
	printf("%d\n",b);       
	printf("%d\n",b--);
	printf("%d\n",b);
	 
	
	
	return 0;
}
